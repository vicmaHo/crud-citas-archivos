    private void crearArchivo() throws IOException{
        File archivo = new File("Archivo.txt");
        // si el archivo existe habilitamos los botones
        if(archivo.exists()){
            habilitarBotones();
        }else{
            JOptionPane.showMessageDialog(this, "El archivo no existe, se ha creado",
                    "Gestion Archivos", JOptionPane.INFORMATION_MESSAGE);
            archivo.createNewFile(); // creo el nuevo archivo
            habilitarBotones();
        }
    }
    
    private void guardarArchivo(){
        try {
            FileWriter salvarArchivo = new FileWriter("Archivo.txt");
            
            for (int i = 0; i < this.tblTablaCitas.getRowCount(); i++) {
                salvarArchivo.write(dtm.getValueAt(i, 0).toString() + "\n");
                salvarArchivo.write(dtm.getValueAt(i, 1).toString() + "\n");
                salvarArchivo.write(dtm.getValueAt(i, 2).toString() + "\n");
                salvarArchivo.write(dtm.getValueAt(i, 3).toString() + "\n");
                salvarArchivo.write(dtm.getValueAt(i, 4).toString() + "\n");
                System.out.println("Se ha guardado");
            }
            salvarArchivo.close();
            System.out.println("se ha cerrado");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error" + e);
        }
    }
    
    private void mostrarArchivo(){
        String id, nombre, medico, motivo, fecha;
        String path = "Archivo.txt";
        
        File arcLectura = new File(path);
        try {
            // cargamos toda la lectura de nuestro archivo
            Scanner lecturaArchivo = new Scanner(arcLectura); 
            
            while(lecturaArchivo.hasNextLine()){ // mientras que la lectura tenga lineas por leer
                // cargamos linea por linea en las variables que hemos declarado
                id = lecturaArchivo.nextLine();
                nombre = lecturaArchivo.nextLine();
                medico = lecturaArchivo.nextLine();
                motivo = lecturaArchivo.nextLine();
                fecha = lecturaArchivo.nextLine();
                
                dtm.addRow(new Object[] {id, nombre, medico, motivo, fecha}); // creo y a la vez cargo el arreglo con los datos
                // de toda la fila
            }
            
        } catch (FileNotFoundException e) {
        }
    }
    
    private void limpiarTabla(){
        int fila = this.tblTablaCitas.getRowCount();
        for (int i = fila - 1; i >= 0 ; i--) { // ciclo for al reves para limpiar la ultima fila
            dtm.removeRow(i);
        }
    }
    
    
    
